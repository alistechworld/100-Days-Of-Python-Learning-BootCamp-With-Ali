# Variables and Data Types

# Storing different types of data in variables

age = 25  # int
height = 5.9 # Float
name = "Ali" # String
is_student = True # Boolean

# Printing the variables
print("Name:",name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Type Conversion Example

age_str = str(age) # Converting integer to string
print("Age as a string", age_str)

# Taking input from the user
fav_number = input("What is your favourite number?")
fav_number = int(fav_number)
print("Your favourit number is:", fav_number)





